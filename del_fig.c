#include "fillit.h"

void	del_fig(int x, int y, t_tetra *p, t_map *map)
{
	map->matrix[y][x] = '.';
	map->matrix[y + p->y[0]][x + p->x[0]] = '.';
	map->matrix[y + p->y[1]][x + p->x[1]] = '.';
	map->matrix[y + p->y[2]][x + p->x[2]] = '.';
}
